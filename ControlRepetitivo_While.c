#include <stdio.h>

int main (int argc, char *argv[]) {
    
    int n = 0, m = 0; 
    printf("Cadena de numeros de 1 en 1.\n");
    while (n <= 10)
    {
        printf("%d\n", n);
        n++; 
    }
    printf("Cadena de numeros de 2 en 2.\n");
    while (m <= 30)
    {
        printf("%d\n", m);
        m = m+2; 
    }
    
    
    
    
    return 0;
}